    <div class="GeoRing">
        <SidePanel>
            <SourceList
                class="SidePanel__row"
                :source="source"
                :groups="SUPPORTED_SOURCES"
                :onChange="onSourceChange"
            />

            <FileUploader
                class="SidePanel__row"
                :onChange="fetchAggregates"
            />

            <DateRange
                class="SidePanel__row"
                :onChange="onDateFilterChange"
            />

            <label class="SidePanel__row GeoRing__name">
                <span class="GeoRing__rowCaption">Analytic Name</span>
                <input v-model.trim="name" :placeholder="autogeneratedName"/>
            </label>

            <div class="GeoRing__controls">
                <table class="GeoRing__metrics">
                    <tbody>
                        <tr>
                            <th>Records Selected</th>
                            <td>{{ totalSelectedFilteredRecords }} (of {{totalFilteredRecords}} matches)</td>
                        </tr>
                        <tr>
                            <th>Total Records</th>
                            <td>{{ totalRecords }}</td>
                        </tr>
                    </tbody>
                </table>

                <BigButton :disabled="!canSubmit" @click="createAnalytic">
                    Create Analytic
                </BigButton>
            </div>

            <RunningAnalyticsList/>
        </SidePanel>

        <BottomPanel class="GeoRing__bottomPanel">
            <table>
                <thead>
                <tr>
                    <th class="GeoRing__checkboxColumn" @click="toggleSelectAll"><input type="checkbox" :disabled="sortedAggregates.length === 0" :checked="isEveryRowSelected" @click.stop="toggleSelectAll"/></th>
                    <th class="GeoRing__identifierColumn" :class="{ 'sorted': sort.field === 'identifier' }" @click="changeSort('identifier')">Identifier <SortCaret :sort="sort" activeWhen="identifier"/></th>
                    <th class="GeoRing__dateColumn" :class="{ 'sorted': sort.field === 'min_date' }" @click="changeSort('min_date')">First Heard <SortCaret :sort="sort" activeWhen="min_date"/></th>
                    <th class="GeoRing__dateColumn" :class="{ 'sorted': sort.field === 'max_date' }" @click="changeSort('max_date')">Last Heard <SortCaret :sort="sort" activeWhen="max_date"/></th>
                    <th class="GeoRing__countColumn" :class="{ 'sorted': sort.field === 'count' }" @click="changeSort('count')">Count <SortCaret :sort="sort" activeWhen="count"/></th>
                    <th :class="{ 'sorted': sort.field === 'file_names' }" @click="changeSort('file_names')">Filename(s) <SortCaret :sort="sort" activeWhen="file_names"/></th>
                </tr>
                <tr class="GeoRing__keywordFilters">
                    <td class="GeoRing__checkboxColumn"><i class="fa fa-filter"/></td>
                    <td class="GeoRing__identifierColumn" :class="{ 'sorted': sort.field === 'identifier' }"><input placeholder="Filter by keyword" v-model.lazy.trim="filters.identifier"/></td>
                    <td class="GeoRing__dateColumn" :class="{ 'sorted': sort.field === 'min_date' }"></td>
                    <td class="GeoRing__dateColumn" :class="{ 'sorted': sort.field === 'max_date' }"></td>
                    <td class="GeoRing__countColumn" :class="{ 'sorted': sort.field === 'count' }"></td>
                    <td :class="{ 'sorted': sort.field === 'file_names' }"><input placeholder="Filter by keyword" v-model.lazy.trim="filters.filename"/></td>
                </tr>
                </thead>
                <tbody>
                <tr v-for="aggregate in sortedAggregates" :class="{ 'isSelected': aggregate.selected }" @click="() => toggleSelect(aggregate)">
                    <td class="GeoRing__checkboxColumn"><input type="checkbox" v-model="aggregate.selected" @click.stop /></td>
                    <td class="GeoRing__identifierColumn" :class="{ 'sorted': sort.field === 'identifier' }">{{ aggregate.identifier }} <span class="GeoRing__quickFilter" title="Show only this identifier" @click.stop @click="() => filters.identifier = filters.identifier === aggregate.identifier ? '' : aggregate.identifier"><i class="fa fa-filter"/></span></td>
                    <td class="GeoRing__dateColumn" :class="{ 'sorted': sort.field === 'min_date' }" :title="'First Heard: ' + aggregate.min_date">{{ aggregate.min_date | formatDateTime }}</td>
                    <td class="GeoRing__dateColumn" :class="{ 'sorted': sort.field === 'max_date' }" :title="'Last Heard: ' + aggregate.max_date">{{ aggregate.max_date | formatDateTime }}</td>
                    <td class="GeoRing__countColumn" :class="{ 'sorted': sort.field === 'count' }">{{ aggregate.count }}</td>
                    <td :class="{ 'sorted': sort.field === 'file_names' }">{{ aggregate.file_names.join(', ') }}</td>
                </tr>
                <tr v-if="!inflightAggregateRequests && !sortedAggregates.length">
                    <td colspan="7" class="GeoRing__aggregatesPlaceholder">No records match selected filtering criteria</td>
                </tr>
                </tbody>
            </table>

            <div
                class="GeoRing__fitMapContentsButton"
                :class="{'GeoRing__fitMapContentsButton--disabled': !sortedAggregates.length}"
                @click="() => fitMapContents()">
                <i class="fa fa-globe"/> Fit all on Map
            </div>

            <LoadingMask
                class="GeoRing__loadingMask"
                caption="Filtering records.  Please wait."
                small
                v-if="inflightAggregateRequests"
            />
        </BottomPanel>

        <LoadingMask
            class="GeoRing__loadingMask"
            caption="Submitting for processing.  Please wait."
            v-if="isSubmitting"
        />
    </div>
