<script>
    import { mapActions, mapMutations } from 'vuex'
    import axios from 'axios'
    import moment from 'moment'

    import BottomPanel from '../BottomPanel.vue'
    import BigButton from '../BigButton.vue'
    import ErrorMask from '../ErrorMask.vue'
    import LoadingMask from '../LoadingMask.vue'
    import RunningAnalyticsList from '../RunningAnalyticsList.vue'
    import SidePanel from '../SidePanel.vue'
    import SourceList from '../SourceList.vue'

    import DateRange from './DateRange.vue'
    import FileUploader from './FileUploader.vue'
    import SortCaret from './SortCaret.vue'

    import * as MapDelegate from './map-delegate'

    import {SOURCE_GROUP_ELEVATION} from '../../constants'

    export default {
        LABEL: 'GeoRing Batch Processing',

        components: {
            BottomPanel,
            DateRange,
            ErrorMask,
            FileUploader,
            RunningAnalyticsList,
            BigButton,
            LoadingMask,
            SidePanel,
            SortCaret,
            SourceList,
        },

        mounted() {
            MapDelegate.activate({
                onPointClick: this.onPointClick,
            })
        },

        beforeDestroy() {
            MapDelegate.deactivate()
        },

        filters: {
            'formatDateTime'(timestamp) {
                return moment.utc(timestamp).format('MM/DD/YYYY HH:MM\\Z')
            },
        },

        data() {
            return {
                SUPPORTED_SOURCES: [SOURCE_GROUP_ELEVATION],
                aggregates: [],
                filters: {
                    identifier: '',
                    minDate: '',
                    maxDate: '',
                    filename: '',
                },
                name: '',
                sort: {
                    field: 'identifier',
                    ascending: true,
                },
                source: '',
                draftedOn: new Date(),
                totalRecords: 0,
                inflightAggregateRequests: 0,  // refcount as these may come in rapid succession
                isSubmitting: false,
            }
        },

        computed: {
            autogeneratedName() {
                return [
                    'GeoRing',
                    moment(this.draftedOn).format('YYYYMMDDHHmm'),  // User's local timezone
                ].join(':')
            },

            canSubmit() {
                return !this.isSubmitting
                    && !this.inflightAggregateRequests
                    && !this.isMissingSelectedAggregates
            },

            isEveryRowSelected() {
                console.debug('[GeoRing] Recomputing `isEveryRowSelected`')
                return this.selectedAggregates.length === this.aggregates.length
            },

            isMissingSelectedAggregates() {
                return this.selectedAggregates.length === 0
            },

            selectedAggregates() {
                console.debug('[GeoRing] Recomputing `selectedAggregates`')
                return this.aggregates.filter(r => r.selected)
            },

            sortedAggregates() {
                const {field, ascending} = this.sort
                return this.aggregates
                    .slice()
                    .sort((a, b) => {
                        const aComparable = Array.isArray(a[field]) ? a[field].join(',') : a[field]
                        const bComparable = Array.isArray(b[field]) ? b[field].join(',') : b[field]
                        if (aComparable > bComparable) {
                            return ascending ? 1 : -1
                        }
                        if (aComparable < bComparable) {
                            return ascending ? -1 : 1
                        }
                        return 0
                    })
            },

            totalFilteredRecords() {
                return this.aggregates.reduce((subtotal, agg) => subtotal + agg.count, 0)
            },

            totalSelectedFilteredRecords() {
                return this.selectedAggregates.reduce((subtotal, agg) => subtotal + agg.count, 0)
            },
        },

        watch: {
            'filters.identifier'() {
                this.fetchAggregates()
            },

            'filters.filename'() {
                this.fetchAggregates()
            },

            selectedAggregates() {
                MapDelegate.renderPoints(this.aggregates)
            },
        },

        methods: {
            ...mapMutations({
                'onError': 'APPEND_ERROR',
            }),

            ...mapActions({
                'onAnalyticCreated': 'appendNewAnalytic',
            }),

            changeSort(field) {
                // Reverse direction
                if (field === this.sort.field) {
                    this.sort.ascending = !this.sort.ascending
                    return
                }

                // Change field
                switch (field) {
                case 'identifier':
                case 'min_date':
                case 'max_date':
                case 'count':
                case 'file_names':
                    this.sort.field = field
                    this.sort.ascending = true
                    break
                default:
                    console.warn('[GeoRing] Unknown sort field "%s"', field)
                    break
                }
            },

            fetchAggregates() {
                const params = {
                    'file_name':  this.filters.filename,
                    'identifier': this.filters.identifier,
                    'min_date':   this.filters.minDate,
                    'max_date':   this.filters.maxDate,
                }

                const cancelToken = new axios.CancelToken(cancel => {
                    if (this.cancelInflightAggregateRequest) {
                        this.cancelInflightAggregateRequest()
                    }
                    this.cancelInflightAggregateRequest = cancel
                })

                this.inflightAggregateRequests++
                axios.get('/api/georing/aggregates', { params, cancelToken })
                    .then(response => {
                        this.inflightAggregateRequests = Math.max(0, this.inflightAggregateRequests - 1)

                        this.totalRecords = response.data.total_count
                        this.aggregates = response.data.aggregates.map(a => {
                            a.selected = true
                            return a
                        })
                    })
                    .catch(err => {
                        this.inflightAggregateRequests = Math.max(0, this.inflightAggregateRequests - 1)

                        if (axios.isCancel(err)) {
                            return
                        }

                        this.onError({
                            heading: 'Could not retrieve aggregates',
                            message: `A system error prevented the listing of record aggregates. If this
                                      problem persists, please contact an XTerrain administrator for
                                      technical support.`,
                        })
                    })
            },

            onDateFilterChange(fromDate, toDate) {
                this.filters.minDate = fromDate
                this.filters.maxDate = toDate
                this.fetchAggregates()
            },

            onPointClick(identifier) {
                const record = this.aggregates.find(r => r.identifier === identifier)
                record.selected = !record.selected
            },

            onSourceChange(value) {
                this.source = value
            },

            fitMapContents() {
                if (!this.selectedAggregates.length) {
                    return  // Nothing to pan to
                }
                MapDelegate.fitContents()
            },

            createAnalytic() {
                const request = {
                    'name': this.name || this.autogeneratedName,
                    'source': this.source,
                    'filters': {
                        'file_name':    this.filters.filename,
                        'identifier':   this.filters.identifier,
                        'identifiers':  this.selectedAggregates.map(r => r.identifier),
                        'min_date':     this.filters.minDate,
                        'max_date':     this.filters.maxDate,
                    },
                }
                console.debug('[GeoRing] Submitting request for processing:\n%s', JSON.stringify(request, null, 2))
                this.isSubmitting = true
                axios.post('/api/georing/new_analytic', request)
                    .then(response => {
                        this.draftedOn = new Date()
                        this.isSubmitting = false

                        this.onAnalyticCreated(response.data.analytic)
                    })
                    .catch(err => {
                        this.isSubmitting = false
                        const {response} = err
                        if (response) {
                            this.onError({
                                heading: 'Could not submit records for processing',
                                message: response.status >= 400 && response.status < 500
                                    ? `The server rejected your request: "${response.data.error}"`
                                    : `A server or network error prevented the submission of these records
                                       for processing.  If this problem persists, please contact an
                                       XTerrain administrator for technical support.`
                            })
                        }
                        else {
                            this.onError({
                                heading: 'Application error has occurred',
                                message: `An application error is preventing normal operation.  If this
                                          problem persists, please contact an XTerrain administrator for
                                          technical support.  (${err.message})`
                            })
                        }
                    })
            },

            toggleSelect(aggregate) {
                aggregate.selected = !aggregate.selected
            },

            toggleSelectAll() {
                const value = !this.isEveryRowSelected
                this.aggregates.forEach(aggregate => {
                    aggregate.selected = value
                })
            },
        },
    }
</script>

